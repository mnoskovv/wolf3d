#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t i;
	size_t length;

	i = 0;
	length = (int)ft_strlen((char *)src);
	while (i < len)
	{
		if (i < length)
			dst[i] = src[i];
		else
			dst[i] = '\0';
		i++;
	}
	return (dst);
}
