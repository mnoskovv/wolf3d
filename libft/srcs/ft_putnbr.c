#include "libft.h"

void	ft_putnbr(int n)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	else if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	i = n % 10;
	j = n / 10;
	if (j > 0)
		ft_putnbr(j);
	ft_putchar(i + 48);
}
