#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	if (ft_strchr(s, c) == NULL)
		return (NULL);
	while (s[i] != '\0')
		i++;
	while (s[i] != c)
		i--;
	return ((char *)s + i);
}
