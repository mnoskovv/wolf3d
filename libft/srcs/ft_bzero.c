#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*current;
	size_t			i;

	current = s;
	i = 0;
	if (n == 0)
		return ;
	while (i < n)
	{
		current[i] = '\0';
		i++;
	}
}
